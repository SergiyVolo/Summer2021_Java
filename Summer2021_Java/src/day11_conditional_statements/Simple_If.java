package day11_conditional_statements;

public class Simple_If {
	
	/*
	 *  conditional statements:
	 *  	- if statement (simple if)
	*/
	public static void main(String[] args) {
		int a = 120;
	 	int b = 100;
			
		if (a > b) {
			System.out.println("Hello Prime Tech students!");
		}
		
		if (a != b) {
			System.out.println("Good evening!");
		}
		
		System.out.println("Text after conditional statements");
		
		
		
		
		
		
		
		
	}

}
