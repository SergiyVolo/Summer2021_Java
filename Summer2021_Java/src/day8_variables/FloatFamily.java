package day8_variables;

public class FloatFamily {

	public static void main(String[] args) {
		
		// 4-byte    4-byte
				float num1 = 4.134f;
				System.out.println(num1);
				
				float num2 = 5;
				System.out.println(num2);  // 5.0 will be printed
				
				double num3 = 7;
				System.out.println(num3);  // 7.0 will be printed
	}

}
