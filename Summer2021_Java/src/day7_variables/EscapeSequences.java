package day7_variables;

public class EscapeSequences {

	public static void main(String[] args) {
		
		// \n - newline escape sequence
		System.out.println("What a beatiful day  \n to practice Java!");
		
		System.out.print("Java is easy!\n");
		System.out.print("Very easy!");
		
		System.out.println();
		System.out.println("======================================================");
		
		System.out.println("Functional testing is also easy!");
		System.out.println("\tSDLC is super easy!");
		
		System.out.println("test\\");
		
		System.out.println("\"Harry Potter\" is my favorite book");
	}
	

}
